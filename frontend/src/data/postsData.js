// 유틸리티 함수들
export const getPostById = (id) => {
  return postsData.find((post) => post.id === parseInt(id));
};

export const getPostsByCategory = (category) => {
  return postsData.filter(
    (post) => post.category.toLowerCase() === category.toLowerCase()
  );
};

export const getFeaturedPosts = () => {
  return postsData.filter((post) => post.featured);
};

export const getPopularPosts = (limit = 5) => {
  return postsData.sort((a, b) => b.views - a.views).slice(0, limit);
};

export const getRecentPosts = (limit = 10) => {
  return postsData
    .sort((a, b) => new Date(b.date) - new Date(a.date))
    .slice(0, limit);
};

export const searchPosts = (query) => {
  const lowercaseQuery = query.toLowerCase();
  return postsData.filter(
    (post) =>
      post.title.toLowerCase().includes(lowercaseQuery) ||
      post.content.toLowerCase().includes(lowercaseQuery) ||
      post.tags.some((tag) => tag.toLowerCase().includes(lowercaseQuery))
  );
};

export const getCommentsByPostId = (postId) => {
  return commentsData.filter((comment) => comment.postId === parseInt(postId));
};
