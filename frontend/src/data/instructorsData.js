// 유틸리티 함수들
export const getInstructorById = (id) => {
  return instructorsData.find((instructor) => instructor.id === parseInt(id));
};

export const getInstructorsBySpecialty = (specialty) => {
  return instructorsData.filter((instructor) =>
    instructor.specialty.some((spec) =>
      spec.toLowerCase().includes(specialty.toLowerCase())
    )
  );
};

export const getTopRatedInstructors = (limit = 3) => {
  return instructorsData.sort((a, b) => b.rating - a.rating).slice(0, limit);
};
